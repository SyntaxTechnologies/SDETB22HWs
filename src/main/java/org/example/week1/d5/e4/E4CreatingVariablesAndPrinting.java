package org.example.week1.d5.e4;

public class E4CreatingVariablesAndPrinting {
    public static void main(String[] args) {

        // The pattern should be:
        //*
        System.out.println("*");
        // **
        System.out.println("**");
        // ***
        System.out.println("***");
        // ##
        System.out.println("##");
        // #
        System.out.println("#");

        // Note: Each System.out.println statement will create a new line

    }
}
