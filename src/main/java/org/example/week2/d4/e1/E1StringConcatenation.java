package org.example.week2.d4.e1;

public class E1StringConcatenation {
    public static void main(String[] args) {

        String firstname, lastname;
        // Step 1: Declare the String variable and set it to "John"
        firstname="John";


        // Step 2: Declare the String variable and set it to "Smith"
        lastname="Smith";


        // Step 3: Print the concatenated name
        System.out.println(firstname+" "+lastname);

    }
}
